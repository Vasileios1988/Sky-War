package skyWar;

import java.io.Serializable;
import java.util.ArrayList;



public class MasterShip implements  Serializable, Observable {

	/**
	 * 
	 */
/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////  Properties  //////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	private String name;
	private static final long serialVersionUID = 1L;
	private ArrayList <EnemyShip> enemies = new ArrayList <EnemyShip>();
	
/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////  Constructor //////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public MasterShip() {
		
	}
	
	
/////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////  Methods ////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public String toString() {
		String output;
		
		output = "MasterShip" + this.getName();
		
		return output;
	}
	
//..........................................................................................................................................................

/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////  Getters and Setters //////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	@Override
	public void registerObserver(EnemyShip enemy) {
		this.enemies.add(enemy);
		
	}


	@Override
	public void removeObserver(EnemyShip enemy) {
		this.enemies.remove(enemy);
		
	}


	@Override
	public void notifyObservers() {
		
		for(EnemyShip e: this.enemies) {
			e.update(false);
		}
		
	}
	
	
	
}// end of MasterShip Class
