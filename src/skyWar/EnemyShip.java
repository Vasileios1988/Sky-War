package skyWar;



import java.io.Serializable;




public abstract class EnemyShip implements Observer, Serializable {
	
	/**
	 * 
	 */
	
/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////  Properties  //////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	private static final long serialVersionUID = 1L;
	private int shipKind;
	private boolean hasMoved; // it will help us with the movement 
	
/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////  Constructor //////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public EnemyShip() {
		
	}
	
/////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////  Methods ////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public String toString() {
		String output = " ";
		
		output = "Enemy Ship: \n ";
		
		return output;
	}

	
//..............................................................................................................................................................
/*
 * 
 * 		Observer Pattern Method
 * 
 * 
 */
	@Override
	public void update(boolean theUpdate) {
		this.update(theUpdate);
		
	}

/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////  Getters and Setters //////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////

	public int getShipKind() {
		return this.shipKind;
	}

	public void setShipKind(int shipKind) {
		this.shipKind = shipKind;
	}

	public boolean isHasMoved() {
		return this.hasMoved;
	}

	public void setHasMoved(boolean hasMoved) {
		this.hasMoved = hasMoved;
	}
	
	
	
	
}
