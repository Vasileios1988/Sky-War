package skyWar;


import java.io.Serializable;
import java.util.ArrayList;

public class Grid implements ProtoType, Serializable {
	/**
	 * 
	 */
/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////  Properties  //////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	private static final long serialVersionUID = 1L;
	private ArrayList <Row> theGrid = new ArrayList <Row> ();
	
/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////  Constructor //////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public Grid() {
		final int NUMBER_OF_ROWS = 4;
		
		for(int loop = 1; loop <= NUMBER_OF_ROWS; loop++) {
			Row row = new Row(loop);
			theGrid.add(row);
		}
	}// end of constructor
	
/////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////  Methods ////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public Row getARow(int num) { // getting a specific row from the grid
		Row tempRow = new Row(num);
		
		for(Row aRow : this.theGrid) {
			if(aRow.getIndex() == num) {
				tempRow = aRow;
			}
		}
		return tempRow;
	}

	
/////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////  Getters and Setters //////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////
	
	public ArrayList<Row> getTheGrid() {
		return this.theGrid;
	}

	public void setTheGrid(ArrayList<Row> theGrid) {
		this.theGrid = theGrid;
	}

	@Override
	public ProtoType getClone() { // to create a clone to helps us with the movement
		// TODO Auto-generated method stub
		return new Grid();
	}
	

}// end of Grid class
